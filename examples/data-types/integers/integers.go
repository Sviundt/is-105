package main

import "fmt"

func main() {
	fmt.Println("1 + 1 =", 1+1)
	// Legger sammmen 1+1 og putter det etter stringen "1 + 1 ="

	fmt.Println("4 / 2 =", 4/2)
	// Deler 4/2 og putter det etter stringen "4 / 2 ="

	fmt.Println("5 * 3 =", 5*3)
	// Multipliserer 5*3 og putter det etter stringen "5 * 3 ="

	fmt.Println("3 - 1 =", 3-1)
	// Trekker 1 fra 3 og putter det etter stringen "3 - 1 ="
}
