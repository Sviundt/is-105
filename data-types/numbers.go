package main

import "fmt"

func main() {
	/* Plusser "Integers", eller "Heltall" som vi kaller dem på norsk.
	Mellom "" er en tekst-streng som ikke blir modifisert, mens tallene på utsiden legges sammen.*/
	fmt.Println("1 + 1 =", 1+1)

	/* "Floating Point Numbers", eller "Desimaltall" som vi kaller dem på norsk.
	Legg merke til at det er spesifisert at det er 0 bak punktum/komma */
	fmt.Println("1 + 1 =", 1.0+1.0)

}
